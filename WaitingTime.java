import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int T=sc.nextInt();
		for(int i=0;i<T;i++){
		    int K=sc.nextInt();
		    int X=sc.nextInt();
		    int d=7*K-X;
		    System.out.println(d);
		}
	}
}
